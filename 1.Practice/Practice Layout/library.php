<?php

function add ($a , $b) {
    echo "The Sum of $a and $b = " . $a + $b . "<br>";
}

function sub ($a , $b) {
    echo "The Difference of $a and $b = " . $a - $b . "<br>";
}

function mult ($a , $b) {
    echo "The Product of $a and $b = " . $a * $b . "<br>";
}

function div ($a , $b) {
    echo "The Division of $a and $b = " . $a / $b . "<br>";
}

?>